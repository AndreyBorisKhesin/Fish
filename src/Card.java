/**
 * A class that represents a card that holds a suit and a value.
 */
public class Card {

    /**
     * Value of card from 0 to 5.
     */
    private int v;

    /**
     * Value of suit from 0 to 7.
     * Odd numbers are high suits.
     * Even numbers are low suits.
     *
     */
    private int s;

}
